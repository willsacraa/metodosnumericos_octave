function I = quadgauss(func, n, a, b)

% Metodo de integração pela quadratura gaussiana
% Lembrar que a funcao deve ser um script, sendo o func uma funcao de x

c = zeros(6);
xd = zeros(6);

c(2,1:2) = [1 1];
c(3,1:3) = [0.5555556 0.8888889 0.5555556];
c(4,1:4) = [0.3478548 0.6521452 0.6521452 0.3478548];
c(5,1:5) = [0.2369269 0.4786287 0.5688889 0.4786287 0.2369269];
c(6,1:6) = [0.1713245 0.3607616 0.4679139 0.4679139 0.3607616 0.1713245];

xd(2,1:2) = [-0.577350269 0.577350269];
xd(3,1:3) = [-0.774596669 0 0.774596669];
xd(4,1:4) = [-0.861136312 -0.339981044 0.339981044 0.861136312];
xd(5,1:5) = [-0.906179846 -0.538469310 0 0.538469310 0.906179846];
xd(6,1:6) = [-0.932469514 -0.661209386 -0.238619186 0.238619186 0.661209386 0.932469514];

% Mudanca de limite de integracao
x = (a+b)/2 + (b-a)/2*xd;

cdx = (b-a)/2;

% Calculo da integral

I = 0;

for i = 1:n
    I = I + c(n,i) * feval(func,x(n,i)) * cdx;
end